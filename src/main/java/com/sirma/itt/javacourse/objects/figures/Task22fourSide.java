public class Task22fourSide extends Figure {
	private int width;
	private int height;

	/**
	 * Getter method for width.
	 * 
	 * @return the width
	 */
	public int getWidth() {
		return width;
	}

	/**
	 * Setter method for width.
	 * 
	 * @param width
	 *            the width to set
	 */
	public void setWidth(int width) {
		this.width = width;
	}

	/**
	 * Getter method for height.
	 * 
	 * @return the height
	 */
	public int getHeight() {
		return height;
	}

	/**
	 * Setter method for height.
	 * 
	 * @param height
	 *            the height to set
	 */

	public void setHeight(int height) {
		this.height = height;
	}
}
